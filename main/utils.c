static char *url_encode (char *data) {

	char *result = malloc (16);
	result[0] = 0x00;

	size_t i;
	for (i=0; i < strlen (data); ++i) {
		if (kUnreservedChar[(int)data[i]]) {
			char *r1;
			asprintf (&r1, "%s%c", result, data[i]);
			free (result);
			result = r1;
		} else {
			char *r1;
			asprintf (&r1, "%s%%%02X", result, data[i]);
			free (result);
			result = r1;
		}
	}

	return result;

}

